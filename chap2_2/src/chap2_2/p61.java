package chap2_2;

public class p61 {

	public static void main(String[] args) {
		
		// 정수 리터럴
		
		int var1 = 0b1011;      //2진수
		int var2 = 0206;        //8진수
		int var3 = 365;         //10진수
		int var4 = 0xB3;        //16진수
		
		System.out.println("var1:" + var1);
		System.out.println("var2:" + var2);
		System.out.println("var3:" + var3);
		System.out.println("var4:" + var4);
	}

}
